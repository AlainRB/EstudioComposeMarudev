@Preview(showBackground = true, showSystemUi = true)
@Composable
fun MyTexts() {
    var messages = message
    LazyColumn() {
        items(messages) { message -> Cuerpo(message) }
    }

}

@Composable
fun Cuerpo(message: MyMessage) {
    val context = LocalContext.current // ðŸ‘ˆ 2. ObtÃ©n el contexto actual aquÃ­
    var expanded by remember { mutableStateOf(false) }

    Row() {
        MyImage()
        Column(
            modifier = Modifier
                .padding(end = 18.dp)
                .clickable {
                    expanded = !expanded
                    Toast.makeText(context, "TEXTO ${message.title}", Toast.LENGTH_SHORT).show()
                }) {
            MyText(text = message.title, MaterialTheme.typography.titleSmall)
            Spacer(modifier = Modifier.height(10.dp))
            MyText(
                text = message.body,
                MaterialTheme.typography.bodyMedium,
                if (expanded) Int.MAX_VALUE else 1
            )
        }
    }
}

@Composable
fun MyImage() {
    Image(
        painterResource(R.drawable.facebook),
        "Imagen de Facebook", modifier = Modifier
            .padding(
                20.dp

            )
            .size(80.dp)
    )

}

@Composable
fun MyText(text: String, style: TextStyle, maxLines: Int = Int.MAX_VALUE) {
    Text(text, style = style, maxLines = maxLines)
}
